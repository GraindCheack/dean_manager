abstract class OffsetConst {
  static const double _defaultSize = 16;
  static const double xxs = _defaultSize / 4;
  static const double xs = _defaultSize / 2;
  static const double s = _defaultSize / 1.5;
  static const double m = _defaultSize;
  static const double l = _defaultSize * 1.5;
  static const double xl = _defaultSize * 2;
  static const double xxl = _defaultSize * 4;
}

